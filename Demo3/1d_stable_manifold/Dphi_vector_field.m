function Dphi = Dphi_vector_field(x)

x1 = x(1); x2 = x(2);

if isintval(x)==1
    i4 = intval(4);
else
    i4 = 4;
end

% p4 = x1^4 + x2^2; 
% q = x1^3/3 - (1 - p4)^2*x1;
% r = x1^2 - x2;
% F = (1/4)*(1 + 3*p4);
% G = x1^3*r + (x2/2)*q;
% 
% F_x1 = 3*x1^3; 
% F_x2 = (3/2)*x2;
% 
% r_x1 = 2*x1; 
% r_x2 = -1;
% 
% q_x1 = x1^2 - (1-p4)*(7*x1^4-x2^2+1); 
% q_x2 = 4*x1*x2*(1-p4);
% 
% G_x1 = 3*x1^2*r + x1^3*r_x1 + (x2/2)*q_x1; 
% G_x2 = x1^3*r_x2 + (1/2)*q + (x2/2)*q_x2;

% Dphi(1,1) = r_x1*F + r*F_x1 - G - x1*G_x1;
% Dphi(1,2) = r_x2*F + r*F_x2 - x1*G_x2;
% Dphi(2,1) = q_x1*F + q*F_x1 - 2*x2*G_x1;
% Dphi(2,2) = q_x2*F + q*F_x2 - 2*G - 2*x2*G_x2;

Dphi11 = x1/2 - 3*x1^5/2 + x1*x2 + x1^3*x2/3 - 6*x1^5*x2 + 5*x1^9*x2 + 3*x1*x2^2/2 - 2*x1*x2^3 + 6*x1^5*x2^3 + x1*x2^5;
Dphi12 = -1/i4 + x1^2/2 + x1^4/12 - x1^6 + x1^10/2 + 3*x1^2*x2/2 - 9*x2^2/4 - 3*x1^2*x2^2 + 3*x1^6*x2^2 + 5*x1^2*x2^4/2;
Dphi21 = -1/i4 + x1^2/4 - 5*x1^4/4 + 7*x1^6/4 + 45*x1^8/4 - 39*x1^12/4 - 10*x1^4*x2 + 3*x2^2/4 + 23*x1^2*x2^2/4 + 5*x1^4*x2^2/2 - 45*x1^8*x2^2/4 - 3*x2^4/4 - 5*x1^4*x2^4/4 + x2^6/4;
Dphi22 = -2*x1^5 + 3*x1*x2/2 + 23*x1^3*x2/6 + x1^5*x2 - 5*x1^9*x2/2 - 3*x1*x2^3 - x1^5*x2^3 + 3*x1*x2^5/2;

Dphi = [[Dphi11 Dphi12];[Dphi21 Dphi22]];

end

