function phi = phi_vector_field(x)

x1 = x(1); x2 = x(2);
% 
% p4 = x1^4 + x2^2; 
% q = x1^3/3 - (1 - p4)^2*x1;
% r = x1^2 - x2;
% F = (1/4)*(1 + 3*p4);
% G = x1^3*r + (x2/2)*q;
% 
% phi(1) = r*F - x1*G ;
% phi(2) = q*F - 2*x2*G;

phi1 = x1^2/4 - x1^6/4 - x2/4 + x1^2*x2/2 + x1^4*x2/12 - x1^6*x2 + x1^10*x2/2 + 3*x1^2*x2^2/4 - 3*x2^3/4 - x1^2*x2^3 + x1^6*x2^3 + x1^2*x2^5/2;
phi2 = -x1/4 + x1^3/12 - x1^5/4 + x1^7/4 + 5*x1^9/4 - 3*x1^13/4 -  2*x1^5*x2 + 3*x1*x2^2/4 + 23*x1^3*x2^2/12 + x1^5*x2^2/2 - 5*x1^9*x2^2/4 - 3*x1*x2^4/4 - x1^5*x2^4/4 + x1*x2^6/4;

phi = [phi1;phi2];

end